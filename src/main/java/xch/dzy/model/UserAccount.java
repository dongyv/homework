package xch.dzy.model;

public class UserAccount {
	private int id;
	private int uid;
	private float use_money;
	private float no_use_money;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getUid() {
		return uid;
	}
	public void setUid(int uid) {
		this.uid = uid;
	}
	public float getUse_money() {
		return use_money;
	}
	public void setUse_money(float use_money) {
		this.use_money = use_money;
	}
	public float getNo_use_money() {
		return no_use_money;
	}
	public void setNo_use_money(float no_use_money) {
		this.no_use_money = no_use_money;
	}
	
}
